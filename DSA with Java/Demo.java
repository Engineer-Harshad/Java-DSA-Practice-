package ycb;
public class Demo {
	public static void main(String[] args) {
		// Print odd numbers between 1 to 10
		// Logic of Odd no.: After dividing the number by 2, the remainder will not be equal to 0
		int i = 1;
		int n = 10;
		while (i<=n) {
		if(i%2 == 0) {
		System.out.print("");
		}
		else {
		System.out.println(i);
		}
		i++;
		}
	}

}
